
console.log("operators in js")

//ARITHMETIC OPERATORS
let a = 5 
let b = 2
console.log("a+b =",a+b)
console.log("a-b =",a-b)
console.log("a*b =",a*b)
console.log("a/b =",a/b)
console.log("a**b =",a**b)
console.log("a%b =",a%b)


console.log("a++ =",a++)  //value then increment    
console.log("++a =",++a)  //increment then value
console.log("a-- =",a--)  //value then decrement
console.log("--a =",--a)  //decrement then value
// 5, 7, 7, 5,


